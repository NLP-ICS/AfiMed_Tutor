def _render_chunks(chunks) -> str:
    parts = []
    for i, c in enumerate(chunks, 1):
        parts.append(f"[Excerpt {i}] {c.source_doc} — {c.section_title}\n{c.text}")
    return "\n\n---\n\n".join(parts) if parts else "(No relevant excerpts found.)"


def _extract_answer(text: str) -> str:
    text = text.strip()
    for char in text:
        if char.upper() in "ABCDE":
            return char.upper()
    return text[:1].upper()


def load_test_items(n: int = 100) -> list[MCQItem]:
    items: list[MCQItem] = []
    with open(TEST_PATH) as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            raw = json.loads(line)
            options = raw.get("options", [])
            if isinstance(options, dict):
                options = [MCQOption(key=k, text=v) for k, v in options.items()]
            else:
                options = [MCQOption(**o) for o in options]
            raw["options"] = options
            items.append(MCQItem(**raw))
            if len(items) >= n:
                break
    return items


def generate_hypothesis(question: str, llm_client) -> str:
    """Generate a hypothetical guideline excerpt for the question."""
    prompt = _HYDE_GENERATION_PROMPT.format(question=question)
    try:
        result = llm_client.complete(
            system=prompt,
            user="Write the guideline excerpt now.",
            max_tokens=200,
            temperature=0.3,
        )
        return result.text.strip()
    except Exception as e:
        log.warning("HyDE generation failed, falling back to raw query: %s", e)
        return question


