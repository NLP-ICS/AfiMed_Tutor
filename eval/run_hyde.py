def write_csv(rows: list[dict], path: Path) -> None:
    if not rows:
        log.warning("No rows to write for %s", path)
        return
    path.parent.mkdir(parents=True, exist_ok=True)
    with open(path, "w", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=list(rows[0].keys()))
        writer.writeheader()
        writer.writerows(rows)
    accuracy = sum(r["correct"] for r in rows) / len(rows)
    log.info("Saved %s  (n=%d, accuracy=%.3f)", path.name, len(rows), accuracy)


def generate_report(rag_rows: list[dict], hyde_rows: list[dict]) -> str:
    from collections import defaultdict

    def accuracy(rows):
        return sum(r["correct"] for r in rows) / len(rows) if rows else 0.0

    def mean_latency(rows):
        return sum(r["latency_ms"] for r in rows) / len(rows) if rows else 0.0

    def by_specialty(rows):
        spec = defaultdict(list)
        for r in rows:
            spec[r["specialty"]].append(r["correct"])
        return {k: sum(v)/len(v) for k, v in spec.items()}

    rag_acc = accuracy(rag_rows)
    hyde_acc = accuracy(hyde_rows)
    delta = hyde_acc - rag_acc
    rag_lat = mean_latency(rag_rows)
    hyde_lat = mean_latency(hyde_rows)

    rag_spec = by_specialty(rag_rows)
    hyde_spec = by_specialty(hyde_rows)
    all_specs = sorted(set(list(rag_spec.keys()) + list(hyde_spec.keys())))

    lines = [
        "# HyDE — Hypothetical Document Embeddings Results",
        "## AfriMed Tutor | ICS554 NLP | Ashesi University\n",
        "### Overall Results\n",
        "| Condition | n | Accuracy | Mean Latency (ms) |",
        "|-----------|---|----------|-------------------|",
        f"| Standard RAG | {len(rag_rows)} | {rag_acc:.3f} | {rag_lat:.0f} |",
        f"| HyDE RAG | {len(hyde_rows)} | {hyde_acc:.3f} | {hyde_lat:.0f} |",
        f"| **Delta (HyDE - RAG)** | — | **{delta:+.3f}** | {hyde_lat - rag_lat:+.0f} |",
        "\n### Accuracy by Specialty\n",
        "| Specialty | RAG | HyDE | Δ |",
        "|-----------|-----|------|---|",
    ]

    for spec in all_specs:
        r = rag_spec.get(spec, 0.0)
        h = hyde_spec.get(spec, 0.0)
        lines.append(f"| {spec} | {r:.3f} | {h:.3f} | {h-r:+.3f} |")

    lines += [
        "\n### Key Findings\n",
        f"- HyDE {'improved' if delta > 0 else 'did not improve'} overall accuracy by {abs(delta):.3f} ({abs(delta)*100:.1f} percentage points)",
        f"- Additional latency cost per query: {hyde_lat - rag_lat:+.0f} ms (one extra LLM call)",
        "- HyDE bridges the semantic gap between exam-style questions and clinical guideline prose",
        "\n*Report auto-generated by eval/run_hyde.py*",
    ]

    return "\n".join(lines)


