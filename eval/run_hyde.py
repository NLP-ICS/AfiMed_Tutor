def main():
    parser = argparse.ArgumentParser(description="HyDE Evaluation")
    parser.add_argument(
        "--condition",
        choices=["rag", "hyde", "both"],
        default="both",
    )
    parser.add_argument(
        "--sample-size", type=int, default=100,
        help="Number of test items (default: 100)",
    )
    args = parser.parse_args()

    log.info("Loading test items (n=%d)...", args.sample_size)
    items = load_test_items(n=args.sample_size)
    log.info("Loaded %d items.", len(items))

    llm_client = build_llm_client()
    retriever = build_retriever()
    RESULTS_DIR.mkdir(parents=True, exist_ok=True)

    rag_rows, hyde_rows = [], []

    rag_path = RESULTS_DIR / "rag.csv"
    hyde_path = RESULTS_DIR / "hyde.csv"

    if args.condition in ("rag", "both"):
        if rag_path.exists():
            log.info("Loading cached RAG results from %s", rag_path)
            with open(rag_path) as f:
                reader = csv.DictReader(f)
                rag_rows = [{**r, "correct": int(r["correct"])} for r in reader]
        else:
            rag_rows = run_rag(items, llm_client, retriever)
            write_csv(rag_rows, rag_path)

    if args.condition in ("hyde", "both"):
        if hyde_path.exists():
            log.info("Loading cached HyDE results from %s", hyde_path)
            with open(hyde_path) as f:
                reader = csv.DictReader(f)
                hyde_rows = [{**r, "correct": int(r["correct"])} for r in reader]
        else:
            hyde_rows = run_hyde(items, llm_client, retriever)
            write_csv(hyde_rows, hyde_path)

    if rag_rows and hyde_rows:
        report = generate_report(rag_rows, hyde_rows)
        report_path = RESULTS_DIR / "hyde_report.md"
        report_path.write_text(report)
        log.info("Report saved to %s", report_path)
        print("\n" + "=" * 60)
        print(report)
        print("=" * 60)
    else:
        log.info("Run both conditions to generate the full report.")


if __name__ == "__main__":
    main()