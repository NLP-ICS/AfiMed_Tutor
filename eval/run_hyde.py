def run_rag(items: list[MCQItem], llm_client, retriever) -> list[dict]:
    """Standard RAG — search with raw question text."""
    rows = []
    for i, item in enumerate(items):
        log.info("[rag] %d/%d  %s", i + 1, len(items), item.question_id)
        t0 = time.perf_counter()
        try:
            chunks = retriever.search(item.question)
            options_str = format_options(item.options)
            prompt = _RAG_PROMPT.format(
                retrieved_chunks=_render_chunks(chunks),
                question=item.question,
                options=options_str,
            )
            result = llm_client.complete(system=prompt, user="", max_tokens=10)
            latency_ms = (time.perf_counter() - t0) * 1000
            predicted = _extract_answer(result.text)
            rows.append({
                "question_id": item.question_id,
                "specialty": item.specialty,
                "gold_answer": item.gold_answer,
                "predicted_answer": predicted,
                "correct": int(predicted == item.gold_answer.strip().upper()),
                "latency_ms": round(latency_ms, 1),
                "input_tokens": result.input_tokens,
                "output_tokens": result.output_tokens,
                "hypothesis": "",
            })
        except Exception as e:
            log.error("RAG failed for %s: %s", item.question_id, e)
    return rows


def run_hyde(items: list[MCQItem], llm_client, retriever) -> list[dict]:
    """HyDE RAG — search with hypothetical guideline excerpt."""
    rows = []
    for i, item in enumerate(items):
        log.info("[hyde] %d/%d  %s", i + 1, len(items), item.question_id)
        t0 = time.perf_counter()
        try:
            # Step 1 — generate hypothetical excerpt
            hypothesis = generate_hypothesis(item.question, llm_client)
            log.info("  HyDE query: %s...", hypothesis[:80])

            # Step 2 — search with hypothesis instead of raw question
            chunks = retriever.search(hypothesis)

            # Step 3 — answer with retrieved chunks
            options_str = format_options(item.options)
            prompt = _RAG_PROMPT.format(
                retrieved_chunks=_render_chunks(chunks),
                question=item.question,
                options=options_str,
            )
            result = llm_client.complete(system=prompt, user="", max_tokens=10)
            latency_ms = (time.perf_counter() - t0) * 1000
            predicted = _extract_answer(result.text)
            rows.append({
                "question_id": item.question_id,
                "specialty": item.specialty,
                "gold_answer": item.gold_answer,
                "predicted_answer": predicted,
                "correct": int(predicted == item.gold_answer.strip().upper()),
                "latency_ms": round(latency_ms, 1),
                "input_tokens": result.input_tokens,
                "output_tokens": result.output_tokens,
                "hypothesis": hypothesis,
            })
        except Exception as e:
            log.error("HyDE failed for %s: %s", item.question_id, e)
    return rows


