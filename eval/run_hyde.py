"""HyDE: Hypothetical Document Embeddings for Query Expansion.

Compares standard dense RAG retrieval against HyDE-enhanced retrieval
on the 100-question MCQ test set.

How HyDE works:
    Instead of searching the guideline corpus with the raw MCQ question,
    we first ask the LLM to generate a hypothetical guideline excerpt that
    would answer the question. We then embed THAT text and use it as the
    search query. Guideline language matches guideline language better.

Usage (Colab):
    # Quick test with 10 items
    !python eval/run_hyde.py --sample-size 10

    # Full evaluation
    !python eval/run_hyde.py --sample-size 100

    # Run one condition only
    !python eval/run_hyde.py --condition hyde --sample-size 100
"""

