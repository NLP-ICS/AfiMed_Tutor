_RAG_PROMPT = """\
You are a medical knowledge assessment system. Using ONLY the provided \
guideline excerpts and your clinical reasoning, select the single best answer. \
Reply with ONLY the option letter (A, B, C, or D). Do not explain.

GUIDELINE EXCERPTS:
{retrieved_chunks}

QUESTION: {question}
OPTIONS:
{options}

Answer:\
"""

_HYDE_GENERATION_PROMPT = """\
You are a clinical guideline assistant. Given the medical question below, \
write a 2-3 sentence excerpt from an African clinical guideline that would \
directly answer it. Write ONLY the guideline excerpt — no preamble, \
no explanation, no option letters.

QUESTION: {question}
"""


